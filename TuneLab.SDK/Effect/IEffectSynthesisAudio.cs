using TuneLab.Foundation;

namespace TuneLab.SDK;

// effect 合成域的音频面（只读活视图）：整段不可分割的音频内容 + 内容变更的区间账本。
// 当前唯一站位是 IEffectSynthesisContext.Input（上游 voice 输出或链上前一个 effect 的输出）；
// 角色由站位的属性名承载，本类型只命名能力——将来如兄弟段只读视图等新站位可直接复用。
public interface IEffectSynthesisAudio
{
    // 段起始（全局采样位置，按 SampleRate 计；全局 0 时刻 = 采样点 0。全局轴 long、段内 int）。
    long SampleOffset { get; }
    int SampleCount { get; }
    int SampleRate { get; }

    // 段内 [offset, offset+destination.Length) 拷出到调用方缓冲；越界非法。
    // 插件心智模型是随机访问一个完整数组（知总长、任取区间读、自带缓冲），但永远拿不到宿主内部
    // 存储的引用——宿主存储形态（连续数组/分页）是实现细节，可无缝演进。
    // 仅数据线程调用（Process 同步前缀物化）——worker 只读拷出后的自有缓冲；局部重合成的引擎只读脏区间±上下文窗。
    void Read(int offset, Span<float> destination);

    // 内容变更的区间账本（可选缓存信息源，数据线程触发）：重 Commit 时按上游实际写入的区间上报，
    // 参数 = (start, count)——start 为**全局采样位置**（本段采样率下的绝对轴，long；全局 0 时刻 = 采样点 0），
    // 即绝对轴上 [start, start+count) 的内容已更新。绝对坐标的理由：内容钉在绝对轴（Resize 扩展/裁剪
    // 保内容按绝对位置对齐），账本随之锚定绝对轴——段几何变更后旧积累区间**无需重定基**、天然继续有效。
    // 读取换算：段内偏移 = start − SampleOffset（区间平铺为参数对，不引入冻结区间类型——与 automation
    // RangeModified、状态段同一判例；区间运算归消费方）。
    // 账本语义：缓存型引擎累积进自己的待处理集、在**成功产出后**才清账（取消/失败不清、不丢更新），
    // 配合电平式 Process 决定「重算哪块」；上游只写了头部新增区就只报头部——局部重合成引擎据此把
    // 重算量压到 O(变更区)。账本是**完备**的：内容变更必经 Write 记账，**几何变更（Resize）按对称差记账**
    // ——裁剪掉的区域照报（绝对轴上「从有到无」也是内容变更，且它是下游边界邻域的上下文：被裁内容曾喂过
    // 邻接输出的计算，下游必须被唤醒、由**下游自决**重算范围——点态引擎自决为零、上下文引擎外扩感受野求交）。
    // 故区间可越出当前段界，消费方外扩自己的上下文窗后与自身范围求交。真无变更的空提交（无写无几何变）才静默；
    // 整段上报只出现在强制失效（首建快照 / 工程采样率变更整体重采——每个样本值都变了，如实而非保守）。
    // 无局部能力的引擎可零订阅（被调到就整段重处理——调度归宿主，无「已提交」脉冲）。
    IActionEvent<long, int> RangeModified { get; }
}
